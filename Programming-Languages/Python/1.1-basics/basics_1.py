greet = "welcome"
floatValue = 12.12

print("hello world")

# / always return float
print(2 / 2)

# // will return int
print(2 // 2)

# type will return type of the value
print(type(greet))
print(type(floatValue))
print(min(12, 24))  # => 12
print(max(12, 24))  # => 24
print(abs(-12))  # => 12
print(int(12.12))  # => 12
print(int(12.92))  # => 12
print(float(12))  # => 12.0
print(13 % 2)

